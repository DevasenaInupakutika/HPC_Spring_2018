# Python program to sum all the items in a list (list1)
# Sum is represented by sum1 (sum1 = 0 or sum1 = '' if the items in the list are numbers or strings respectively)

#Example list
list1 = [5,6,7,8,9]

sum1 = 0

for i in list1:
    sum1 = sum1 + i


print("Sum of all the items in list :", sum1)



